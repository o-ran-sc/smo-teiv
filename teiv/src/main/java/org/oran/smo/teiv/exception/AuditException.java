package org.oran.smo.teiv.exception;

import org.springframework.http.HttpStatus;

import lombok.AccessLevel;
import lombok.Builder;
import lombok.Getter;
import lombok.RequiredArgsConstructor;

@Getter
@Builder
@RequiredArgsConstructor(access = AccessLevel.PRIVATE)
public class AuditException extends RuntimeException {
    private final HttpStatus status;
    private final String message;
    private final String details;
    private final Exception exception;

    public static AuditException resourceNotFoundException() {
        return AuditException.builder().status(HttpStatus.NOT_FOUND).message("Resource Not Found").details(
                "The requested adapter id is not found").build();
    }
}
